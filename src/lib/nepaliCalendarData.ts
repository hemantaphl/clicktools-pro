// Reference date for conversion: 1943-04-14 AD = 2000-01-01 BS
const referenceAD = new Date(1943, 3, 14); // April 14, 1943
const referenceBS = { year: 2000, month: 1, day: 1 };

// Get total days in a BS year
export function getTotalDaysInBSYear(year: number): number {
  const monthDays = nepaliCalendarData[year];
  if (!monthDays) return 365;
  return monthDays.reduce((sum, days) => sum + days, 0);
}

// Get days in a specific BS month
export function getDaysInBSMonth(year: number, month: number): number {
  const monthDays = nepaliCalendarData[year];
  if (!monthDays || month < 1 || month > 12) return 30;
  return monthDays[month - 1];
}

// Convert AD to BS
export function adToBS(adDate: Date): { year: number; month: number; day: number } {
  // Calculate days difference from reference
  const diffTime = adDate.getTime() - referenceAD.getTime();
  let daysDiff = Math.floor(diffTime / (1000 * 60 * 60 * 24));
  
  let bsYear = referenceBS.year;
  let bsMonth = referenceBS.month;
  let bsDay = referenceBS.day;
  
  // Add days
  daysDiff += bsDay - 1;
  
  while (daysDiff >= 0) {
    const daysInMonth = getDaysInBSMonth(bsYear, bsMonth);
    
    if (daysDiff < daysInMonth) {
      bsDay = daysDiff + 1;
      break;
    }
    
    daysDiff -= daysInMonth;
    bsMonth++;
    
    if (bsMonth > 12) {
      bsMonth = 1;
      bsYear++;
    }
  }
  
  // Handle negative difference (dates before reference)
  while (daysDiff < 0) {
    bsMonth--;
    if (bsMonth < 1) {
      bsMonth = 12;
      bsYear--;
    }
    daysDiff += getDaysInBSMonth(bsYear, bsMonth);
  }
  
  if (daysDiff >= 0) {
    bsDay = daysDiff + 1;
  }
  
  return { year: bsYear, month: bsMonth, day: bsDay };
}

// Convert BS to AD
export function bsToAD(bsYear: number, bsMonth: number, bsDay: number): Date {
  // Calculate days from reference BS to given BS date
  let totalDays = 0;
  
  if (bsYear > referenceBS.year || (bsYear === referenceBS.year && bsMonth > referenceBS.month) || 
      (bsYear === referenceBS.year && bsMonth === referenceBS.month && bsDay >= referenceBS.day)) {
    // Date is after or on reference
    for (let y = referenceBS.year; y < bsYear; y++) {
      totalDays += getTotalDaysInBSYear(y);
    }
    for (let m = 1; m < bsMonth; m++) {
      totalDays += getDaysInBSMonth(bsYear, m);
    }
    totalDays += bsDay - 1;
  } else {
    // Date is before reference
    for (let y = bsYear; y < referenceBS.year; y++) {
      totalDays -= getTotalDaysInBSYear(y);
    }
    for (let m = 1; m < bsMonth; m++) {
      totalDays -= getDaysInBSMonth(bsYear, m);
    }
    totalDays -= bsDay - 1;
    totalDays = -totalDays;
  }
  
  const adDate = new Date(referenceAD);
  adDate.setDate(adDate.getDate() + totalDays);
  return adDate;
}

// Validate BS date
export function isValidBSDate(year: number, month: number, day: number): boolean {
  if (!nepaliCalendarData[year]) return false;
  if (month < 1 || month > 12) return false;
  const daysInMonth = getDaysInBSMonth(year, month);
  return day >= 1 && day <= daysInMonth;
}

// Get available BS years
export function getAvailableBSYears(): number[] {
  return Object.keys(nepaliCalendarData).map(Number).sort((a, b) => a - b);
}
